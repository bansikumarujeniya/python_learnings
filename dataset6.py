def get_web_series_details(dataset):

    web_series_list = []

    for i in range(len(dataset["title"])):
        if dataset["type"][i] == "Web Series":
            web_series_details = {
                "title": dataset["title"][i],
                "director": dataset["director"][i],
                "rating_imdb": dataset["rating_imdb"][i]
            }
            web_series_list.append(web_series_details)
