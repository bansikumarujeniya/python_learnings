#9. Write a function that takes a genre as input and returns a list of dictionaries, where each dictionary contains the title, platform, and rating_imdb for all entries matching that genre. For example, get_movies_by_genre('Crime') should return a list of dictionaries.

def get_by_genre(input_genre):

    result = []
    for i in range(len(movies_data['title'])):
        if input_genre.lower() in movies_data['genre'][i].lower():
            entry_dict = {
                "title": movies_data['title'][i],
                "platform": movies_data['platform'][i],
                "rating_imdb": movies_data['rating_imdb'][i]
            }
            result.append(entry_dict)
    return result

