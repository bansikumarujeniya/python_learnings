#5. Write a function that counts how many birds belong to each family and returns the result as a new dictionary where the keys are the family names and the values are the counts.
def count_birds_by_family(dataset):
    family_counts = {}
    for bird_info in dataset.values():
        family_name = bird_info.get("family")
        if family_name:
            family_counts[family_name] = family_counts.get(family_name, 0) + 1
    return family_counts

