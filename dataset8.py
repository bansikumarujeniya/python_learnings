#5. In the birds_dataset, some scientific names (scientific_name) are just a family name, indicating that the common name covers multiple species (e.g., "Hummingbird" has a scientific name of "Trochilidae"). Write a function that finds all common names where the scientific name is the same as the family name and returns them in a tuple.
def find_common_names_with_family_as_scientific_name(dataset):
    matching_birds = []
    for bird_info in dataset.values():
        if bird_info.get('scientific_name') == bird_info.get('family'):
            matching_birds.append(bird_info.get('common_name'))

    return tuple(matching_birds)

