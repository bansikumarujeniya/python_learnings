#10. Create a list of tuples, where each tuple contains the common_name and gestation_period_days. Then, sort this list of tuples based on the gestation period (by the lowest number, if it's a range like "640-660").
import re

common_name_gestation = list(zip(animal_dataset["common_name"], animal_dataset["gestation_period_days"]))

def get_lowest_gestation(gestation_string):
    match = re.search(r'\d+', gestation_string)
    if match:
        return int(match.group())
    return 0

sorted_by_gestation = sorted(common_name_gestation, key=lambda item: get_lowest_gestation(item[1]))

for animal in sorted_by_gestation:
    print(animal)

